public class Line {
    private int row;
    private int col;
    private boolean filled;

    public Line(int row, int col, boolean filled) {
        this.row = row;
        this.col = col;
        this.filled = filled;
    }

    public boolean isFilled() {
        return filled;
    }

    @Override
    public boolean equals(Object o) {
        if (this == o) return true;
        if (o == null || getClass() != o.getClass()) return false;

        Line line = (Line) o;

        return (row == line.row && col == line.col && filled == line.filled);
    }

    /**
     * @return Unique hash code generated by "row, col, 1 or 0 depending on filled"
     */
    @Override
    public int hashCode() {
        String coordinates = Integer.toString(row) + "," + Integer.toString(col) + "," + (filled ? "1" : "0");
        return coordinates.hashCode();
    }

}